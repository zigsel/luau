/// Compile the vendored Luau C++ into one static library.
fn buildLuauLib(
    b: *std.Build,
    target: std.Build.ResolvedTarget,
    optimize: std.builtin.OptimizeMode,
    dep: *std.Build.Dependency,
    vector_size: u32,
    enable_codegen: bool,
) *std.Build.Step.Compile {
    const mod = b.createModule(.{
        .target = target,
        .optimize = optimize,
        .link_libc = true,
        .link_libcpp = true,
        // Luau's VM relies on tagged pointers / deliberate integer wraparound that
        // Zig's C UBSan flags as errors; the upstream build does not use UBSan.
        .sanitize_c = .off,
    });

    // public include directories (Luau/*.h and the C headers)
    mod.addIncludePath(dep.path("Common/include"));
    mod.addIncludePath(dep.path("Ast/include"));
    mod.addIncludePath(dep.path("Bytecode/include"));
    mod.addIncludePath(dep.path("Compiler/include"));
    mod.addIncludePath(dep.path("Config/include"));
    mod.addIncludePath(dep.path("Require/include"));
    mod.addIncludePath(dep.path("Analysis/include"));
    mod.addIncludePath(dep.path("VM/include"));
    mod.addIncludePath(dep.path("CodeGen/include"));
    // private source directories (internal headers included by name)
    mod.addIncludePath(dep.path("Compiler/src"));
    mod.addIncludePath(dep.path("Analysis/src"));
    mod.addIncludePath(dep.path("Require/src"));
    mod.addIncludePath(dep.path("VM/src"));
    mod.addIncludePath(dep.path("CodeGen/src"));

    // Give the public API C linkage and use setjmp/longjmp for error propagation
    // (rather than C++ exceptions) so it is safe and ergonomic to call from Zig.
    mod.addCMacro("LUA_API", "extern \"C\"");
    mod.addCMacro("LUACODE_API", "extern \"C\"");
    mod.addCMacro("LUACODEGEN_API", "extern \"C\"");
    mod.addCMacro("LUA_USE_LONGJMP", "1");
    mod.addCMacro("LUA_VECTOR_SIZE", b.fmt("{d}", .{vector_size}));

    const flags: []const []const u8 = &.{
        "-std=c++17",
        "-fno-math-errno", // let the compiler lower sqrt() etc. to a single instruction
    };

    mod.addCSourceFiles(.{ .root = dep.path("Common/src"), .files = common_src, .flags = flags, .language = .cpp });
    mod.addCSourceFiles(.{ .root = dep.path("Ast/src"), .files = ast_src, .flags = flags, .language = .cpp });
    mod.addCSourceFiles(.{ .root = dep.path("Bytecode/src"), .files = bytecode_src, .flags = flags, .language = .cpp });
    mod.addCSourceFiles(.{ .root = dep.path("Compiler/src"), .files = compiler_src, .flags = flags, .language = .cpp });
    mod.addCSourceFiles(.{ .root = dep.path("Config/src"), .files = config_src, .flags = flags, .language = .cpp });
    mod.addCSourceFiles(.{ .root = dep.path("Require/src"), .files = require_src, .flags = flags, .language = .cpp });
    mod.addCSourceFiles(.{ .root = dep.path("Analysis/src"), .files = analysis_src, .flags = flags, .language = .cpp });
    mod.addCSourceFiles(.{ .root = dep.path("VM/src"), .files = vm_src, .flags = flags, .language = .cpp });
    if (enable_codegen) {
        mod.addCSourceFiles(.{ .root = dep.path("CodeGen/src"), .files = codegen_src, .flags = flags, .language = .cpp });
    }

    // our extern "C" shims over the Luau C++ APIs — every shim/**/*.cpp, so a new
    // module is just new files (no build edit needed).
    mod.addIncludePath(b.path("shim"));
    mod.addCSourceFiles(.{ .root = b.path("shim"), .files = collectCpp(b, "shim"), .flags = flags, .language = .cpp });

    return b.addLibrary(.{ .name = "luau", .root_module = mod, .linkage = .static });
}

/// Recursively collect every `*.cpp` under `root`, returning paths relative to
/// `root` (so `shim/**/*.cpp` is compiled without listing files by hand).
fn collectCpp(b: *std.Build, root: []const u8) []const []const u8 {
    const io = b.graph.io;
    var dir = b.build_root.handle.openDir(io, root, .{ .iterate = true }) catch |e|
        std.debug.panic("cannot open '{s}': {s}", .{ root, @errorName(e) });
    defer dir.close(io);
    var walker = dir.walk(b.allocator) catch @panic("oom");
    defer walker.deinit();
    var files: std.ArrayList([]const u8) = .empty;
    while (walker.next(io) catch @panic("walk failed")) |entry| {
        if (entry.kind == .file and std.mem.endsWith(u8, entry.path, ".cpp"))
            files.append(b.allocator, b.dupe(entry.path)) catch @panic("oom");
    }
    return files.toOwnedSlice(b.allocator) catch @panic("oom");
}

