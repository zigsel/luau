const std = @import("std");

/// luau: complete, idiomatic Zig bindings to the Luau C API.
///
/// This build:
///   1. compiles the vendored Luau C++ sources (Common, Ast, Bytecode, Compiler,
///      VM and optionally CodeGen) into a single static library, giving the public
///      `lua_*` / `luau_*` symbols `extern "C"` linkage and LUA_USE_LONGJMP error
///      handling (the configuration Luau documents for use from non-C++ languages);
///   2. runs `translate-c` over the public headers to produce the raw `bindings`
///      module (ABI-correct declarations that resolve against the static library);
///   3. exposes the idiomatic `luau` module (src/root.zig) layered on top.
pub fn build(b: *std.Build) void {
    const target = b.standardTargetOptions(.{});
    const optimize = b.standardOptimizeOption(.{});

    // ---- user-facing options -------------------------------------------------
    const vector_size = b.option(
        u32,
        "vector_size",
        "LUA_VECTOR_SIZE: number of float components in the native vector type, 3 or 4 (default 3)",
    ) orelse 3;
    if (vector_size != 3 and vector_size != 4) {
        std.debug.panic("vector_size must be 3 or 4, got {d}", .{vector_size});
    }
    const enable_codegen = b.option(
        bool,
        "codegen",
        "Build the Luau native code generation (JIT) backend (default true)",
    ) orelse true;

    const luau_dep = b.dependency("luau_src", .{});

    // ---- compile-time configuration visible to Zig code ----------------------
    const opts = b.addOptions();
    opts.addOption(u32, "vector_size", vector_size);
    opts.addOption(bool, "codegen", enable_codegen);
    const config_mod = opts.createModule();

    // ---- the vendored Luau C++ static library --------------------------------
    const luau_lib = buildLuauLib(b, target, optimize, luau_dep, vector_size, enable_codegen);
    b.installArtifact(luau_lib);

    // ---- raw bindings via translate-c ----------------------------------------
    // The umbrella header is generated inline (rather than a checked-in src/c.h)
    // so the full set of translated headers lives in one place: this build file.
    // translate-c sees LUA_API/… as plain `extern`; the static library gives the
    // same symbols `extern "C"` linkage, so declarations resolve 1:1.
    const umbrella = b.addWriteFiles().add("luau_bindings.h",
        \\// Generated by build.zig — the complete C-callable Luau surface fed to translate-c.
        \\#include "lua.h"          // core VM C API (+ luaconf.h)
        \\#include "lualib.h"       // standard libraries + luaL_* helpers
        \\#include "luacode.h"      // Luau source -> bytecode compiler
        \\#include "luacodegen.h"   // native code generation (JIT)
        \\#include "Luau/Require.h" // require-by-string (already a C API)
        \\#include "Luau/Bytecode.h"// bytecode opcode/type enums
        \\#include "shim.h"    // extern "C" shims over the Luau C++ APIs
        \\
    );
    const tc = b.addTranslateC(.{
        .root_source_file = umbrella,
        .target = target,
        .optimize = optimize,
        .link_libc = true,
    });
    tc.addIncludePath(luau_dep.path("VM/include"));
    tc.addIncludePath(luau_dep.path("Compiler/include"));
    tc.addIncludePath(luau_dep.path("CodeGen/include"));
    tc.addIncludePath(luau_dep.path("Require/include"));
    tc.addIncludePath(luau_dep.path("Common/include"));
    tc.addIncludePath(b.path("shim"));
    // Parse as C: LUA_API expands to plain `extern` (the library gives the real
    // symbols `extern "C"` linkage, so the names still match 1:1).
    tc.defineCMacro("LUA_API", "extern");
    tc.defineCMacro("LUACODE_API", "extern");
    tc.defineCMacro("LUACODEGEN_API", "extern");
    tc.defineCMacro("LUA_VECTOR_SIZE", b.fmt("{d}", .{vector_size}));
    const c_mod = tc.createModule();

    // ---- the idiomatic, public module ----------------------------------------
    const luau_mod = b.addModule("luau", .{
        .root_source_file = b.path("src/root.zig"),
        .target = target,
        .optimize = optimize,
        .link_libc = true,
        .link_libcpp = true,
    });
    luau_mod.addImport("bindings", c_mod);
    luau_mod.addImport("config", config_mod);
    luau_mod.linkLibrary(luau_lib);

    // ---- tests ---------------------------------------------------------------
    // The suite lives in tests/ and consumes the public `luau` module exactly as
    // a downstream user would.
    const test_mod = b.createModule(.{
        .root_source_file = b.path("tests/root.zig"),
        .target = target,
        .optimize = optimize,
    });
    test_mod.addImport("luau", luau_mod);

    const tests = b.addTest(.{ .root_module = test_mod });
    const run_tests = b.addRunArtifact(tests);
    const test_step = b.step("test", "Run the library test suite");
    test_step.dependOn(&run_tests.step);

    // ---- coverage: gate the C API, report the C++ surface honestly -----------
    const cov_run = b.addSystemCommand(&.{"sh"});
    cov_run.addFileArg(b.path("tools/coverage.sh")); // absolute: runs from any cwd
    cov_run.addFileArg(tc.getOutput()); // (A) authoritative list of bound symbols
    cov_run.addDirectoryArg(b.path("src")); //      the idiomatic wrappers (lua/ + luau/)
    cov_run.addDirectoryArg(b.path("shim")); //     our shimmed C++ entry points (shim/**/*.h)
    // (B) Luau's C++ public headers, for the informational surface report:
    cov_run.addDirectoryArg(luau_dep.path("Ast/include"));
    cov_run.addDirectoryArg(luau_dep.path("Compiler/include"));
    cov_run.addDirectoryArg(luau_dep.path("Config/include"));
    cov_run.addDirectoryArg(luau_dep.path("Bytecode/include"));
    cov_run.addDirectoryArg(luau_dep.path("CodeGen/include"));
    cov_run.addDirectoryArg(luau_dep.path("Analysis/include"));
    cov_run.has_side_effects = true;
    const cov_step = b.step("coverage", "Gate C-API coverage; report C++ surface coverage");
    cov_step.dependOn(&cov_run.step);

    // ---- api: inventory the real public C++ surface (clang AST) --------------
    const api_run = b.addSystemCommand(&.{"python3"});
    api_run.addFileArg(b.path("tools/api_inventory.py")); // absolute: runs from any cwd
    api_run.addDirectoryArg(luau_dep.path(".")); // the vendored Luau source root
    if (b.args) |args| api_run.addArgs(args); // e.g. `zig build api -- --list Ast`
    api_run.has_side_effects = true;
    const api_step = b.step("api", "Inventory Luau's public C++ API (what matters vs internal)");
    api_step.dependOn(&api_run.step);

    // ---- gaps: per-module shim coverage gap map (dedicated, no `--` needed) ---
    const gaps_run = b.addSystemCommand(&.{"python3"});
    gaps_run.addFileArg(b.path("tools/api_inventory.py"));
    gaps_run.addDirectoryArg(luau_dep.path("."));
    gaps_run.addArg("--gaps");
    if (b.args) |args| gaps_run.addArgs(args); // optional module, e.g. `zig build gaps -- Ast`
    gaps_run.has_side_effects = true;
    const gaps_step = b.step("gaps", "Per-module shim coverage gap map (add -- <Module> to drill in)");
    gaps_step.dependOn(&gaps_run.step);

    // ---- examples ------------------------------------------------------------
    const example_step = b.step("examples", "Build and run the examples");
    for (examples) |ex| {
        const ex_mod = b.createModule(.{
            .root_source_file = b.path(ex.path),
            .target = target,
            .optimize = optimize,
        });
        ex_mod.addImport("luau", luau_mod);
        const ex_exe = b.addExecutable(.{ .name = ex.name, .root_module = ex_mod });
        b.installArtifact(ex_exe);
        const run = b.addRunArtifact(ex_exe);
        if (b.args) |args| run.addArgs(args);
        const single = b.step(ex.name, b.fmt("Run the '{s}' example", .{ex.name}));
        single.dependOn(&run.step);
        example_step.dependOn(&run.step);
    }
}

